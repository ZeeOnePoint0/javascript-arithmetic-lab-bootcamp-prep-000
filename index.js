function add(a, b) {
  return (a + b);
}

function subtract(a, b) {
  return (b - a);
}

function multiply() {
  return (a * b);
}

function divide() {
  return (a / b);
}

function inc(a) {
  return a++;
}

function dec(a) {
  return a--
}

function parsein(a) {
  return parseInt('a', 10);
}

function pFloat(a) {
  return parseFloat('a');
}
