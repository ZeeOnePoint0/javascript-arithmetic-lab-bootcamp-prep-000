function add(a, b) {
  return (a + b);
}

function subtract(b, a) {
  return (b - a);
}

function multiply(a, b) {
  return (a * b);
}

function divide(a, b) {
  return (a / b);
}

function inc(a) {
  return a+=1;
}

function dec(a) {
  return a-=1;
}

function makeInt(n) {
  n = 0;
  return parseInt('n', 10);
}

function pFloat(a) {
  return parseFloat('a');
}
